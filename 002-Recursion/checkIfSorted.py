# ---------------------------------------------------------------------------- #
#                            Check for sorted Array                            #
# ---------------------------------------------------------------------------- #

def isSorted(arr):
    if len(arr) == 1:
        return True
    return arr[0] < arr[1] and isSorted(arr[1:])


arr = [12, 23, 31, 38, 44, 49, 89, 991, 1034, 4098]
print(isSorted(arr))

# ---------------------------------------------------------------------------- #
#                             Time Complexity: O(n)                            #
#                   Space Complexity: O(n) - recursive stack                   #
# ---------------------------------------------------------------------------- #