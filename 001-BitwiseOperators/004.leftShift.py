'''
Left Shift Operator ( << ): It shifts the bit right by the amount specified and adds 0 in same amount to the RHS of the number, thus it multiplies the number.

i.e x << y, this implies that resultant is x * (2^y)
'''

x = 3

print(x << 1) # 6
print(x << 2) # 12
print(x << 3) # 24
print(x << 4) # 48