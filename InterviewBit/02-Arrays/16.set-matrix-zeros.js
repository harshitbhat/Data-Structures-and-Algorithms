module.exports = {
  /**
   * param A: list of list of integers
   * @return the same list modified
   */
  solve: function (mat) {
    const toZero = [];
    const n = mat.length;
    for (let i = 0; i < n; i++) {
      for (let j = 0; j < n; j++) {
        if (mat[i][j] === 1) {
          toZero.push([i, j]);
        }
      }
    }

    for (const el of toZero) {
      const row = el[0];
      const col = el[1];

      for (let i = 0; i < n; i++) {
        mat[i][col] = 0;
        mat[row][i] = 0;
      }
    }
    return mat;
  },
};

function solve(mat) {
  const m = mat.length;
  const n = mat[0].length;

  const rows = {};
  const cols = {};

  for (let i = 0; i < m; i++) {
    for (let j = 0; j < n; j++) {
      if (mat[i][j] === 0) {
        rows[i] = true;
        cols[j] = true;
      }
    }
  }

  for (let i = 0; i < m; i++) {
    for (let j = 0; j < n; j++) {
      if (rows[i] || cols[j]) {
        mat[i][j] = 0;
      }
    }
  }

  return mat;
}

