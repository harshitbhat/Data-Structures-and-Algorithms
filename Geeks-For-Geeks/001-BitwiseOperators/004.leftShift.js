/*

Left Shift Operator ( << ): It shifts the bit left by the amount specified and adds 0 in same amount to the right side of the number, thus it multiplies the number.

i.e x << y, this implies that resultant is x * (2^y)

*/
const x = 3;

console.log(x << 1);
console.log(x << 2);
console.log(x << 3);
console.log(x << 4);
