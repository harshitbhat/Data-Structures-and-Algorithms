/*

Right Shift Operator ( >> ): It shifts the bit right by the amount specified and adds 0 in same amount to the left side of the number, thus it divides the number.

i.e x << y, this implies that resultant is floor of  x / (2^y)


*/

const x = 55;

console.log(x >> 1);
console.log(x >> 2);
console.log(x >> 3);
console.log(x >> 4);
